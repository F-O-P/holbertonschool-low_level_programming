#include "3-calc.h"

/**
 * op_add - fill
 * @a: get first int
 * @b: get second int
 * Return: sum of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - fill
 * @a: get first int
 * @b: get second int
 * Return: difference of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - fill
 * @a: get first int
 * @b: get second int
 * Return: multiple of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fill
 * @a: get first int
 * @b: get second int
 * Return: a divided by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fill
 * @a: get first int
 * @b: get second int
 * Return: modulus of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
